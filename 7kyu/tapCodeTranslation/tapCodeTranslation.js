tapCodeTranslation = (text) => {
  const DOTS = {
    a: ". .",
    b: ". ..",
    c: ". ...",
    k: ". ...",
    d: ". ....",
    e: ". .....",
    f: ".. .",
    g: ".. ..",
    h: ".. ...",
    i: ".. ....",
    j: ".. .....",
    l: "... .",
    m: "... ..",
    n: "... ...",
    o: "... ....",
    p: "... .....",
    q: ".... .",
    r: ".... ..",
    s: ".... ...",
    t: ".... ....",
    u: ".... .....",
    v: "..... .",
    w: "..... ..",
    x: "..... ...",
    y: "..... ....",
    z: "..... .....",
  };
  return [...text].map((letter) => DOTS[letter]).join(" ");
};

module.exports = tapCodeTranslation;
